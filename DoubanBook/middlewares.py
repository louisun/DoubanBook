class UserAgent(object): # 401
    def process_requests(self, request, spider):
        request.headers["User-Agent"] = USER_AGENT


class SetCookie(object): # 701
    def __init__(self):
        self.bids = gen_bids()

    def process_requests(self, request, spider):
        request.headers["Cookie"] = 'bids="%s"' % random.choice(self.bids)


class CustomHeaders(object): # 551
    def process_requests(self, request, spider):
        request.headers["Accept-Language"] = "zh-CN,zh"

class ProxyMiddleware(object):
    def process_request(self, request, spider):
        request.meta['proxy'] = random.choice(settings['PROXY_POOL'])